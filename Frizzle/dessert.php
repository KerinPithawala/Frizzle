								<h3>Double Chocolaty Chip Crème Frappuccino</h3>
								<ul>
									<li>
										This drink feels amazing to hold in the hand on a warm Sunday afternoon.Gazing inside, one is drawn in by the fine texture of brown tones, punctuated here and there by dark chocolate chips. Up top the cream dresses things up a bit, with a sprinkling of what Starbucks calls "mocha drizzle".The cream is light and sweet, perhaps sweeter than the Frappuccino itself, but not overbearing - a pleasant hum from a single cloud in the sky. The mocha drizzle (chocolate syrup) is present only in a quantity that enables it to color the experience, not determine the core essence its ultimate manifestation, simply adding a slight celebratory lilt to the wispy song.
									</li>
								</ul>
								<h2>Ingredients:</h2>
								<ul class="ingrdients">
									<li>
										1 cup milk – regular or low fat work best, but for a lighter version use non-fat skim milk
									</li>
									<li>
										1 ½ cups ice
									</li>
									<li>
										¼ cup chocolate syrup
									</li>
									<li>
										½ cup mini semi-sweet chocolate chips
									</li>
									<li>
										½ tsp. pure vanilla extract or vanilla bean paste
									</li>
									<li>
										1 ½ tsp. instant coffee dissolved in 1 teaspoon hot water
									</li>
									<li>
										2 Tbs. white granulated sugar dissolved in 2 Tbs. hot water
									</li>
									<li>
										Topping:
										<ul>
											<li>
												Whipped cream
											</li>
											<li>
												Chocolate syrup
											</li>
											<li>
												Mini semi-sweet chocolate chips or chocolate sprinkles
											</li>
										</ul>
									</li>
								</ul>
								<h2>Directions:</h2>
								<ul class="ingrdients">
									<li>
										1.	Add milk and syrup into your blender and blend.
									</li>
									<li>
										2.	Next add your ice and chocolate chips. Blend until chocolate chips look like small specs.
									</li>
									
								</ul>
