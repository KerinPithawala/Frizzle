								<h3>Iced Pineapple Matcha Drink</h3>
								<ul>
									<li>
										The beverage features flavours that I enjoy separately — matcha green tea, pineapple, ginger, and coconut milk, all of which are shaken with ice. The matcha and pineapple are the most notable flavours competing for your taste buds, and to be honest, they are strange cup fellows. The coconut milk enhances the flavour of the pineapple, giving the whole drink a pina colada undertone. As for the ginger, it’s light and not noticeable with every sip. I’m not sure why it’s there.
									</li>
								</ul>
								<h2>Ingredients:</h2>
								<ul class="ingrdients">
									<li>
										1 Cup Coconut Milk
									</li>
									<li>
										2 tsp Matcha Powder 
									</li>
									<li>
										1.5 Tbsp Pineapple Syrup
									</li>
									<li>
										1/4 tsp Ground Ginger
									</li>
									
								</ul>
								<h2>Directions:</h2>
								<ul class="ingrdients">
									<li>
										1.	Add sugar, pineapple juice, water and ginger to a large mason jar and microwave for 5 minutes until mixture is boiling to make the syrup. Chill in the fridge.
									</li>
									<li>
										2.	Meanwhile, assemble ingredients for your drink when ready.
									</li>
									<li>
										3.	Add pineapple ginger syrup, matcha tea, pineapple juice and coconut milk to a cocktail shaker or shaker bottle. Shake to combine.
									</li>
									<li>
										4.	Add a handful of ice to a large glass.
									</li>
									<li>
										5.	Pour drink overtop.
									</li>
									<li>
										6.	Serve and enjoy!
									</li>
								</ul>
