								<h3>Pumpkin Spice Latte</h3>
								<ul>
									<li>
										The Pumpkin Spice Latte is like an autumnal party for your taste buds. . Espresso and steamed milk are combined with a blend of pumpkin and traditional fall spice flavors. With hints of cinnamon, nutmeg, and clove, you'll feel like your right at home wearing a cozy sweater, even if you're still struggling with humidity and 90-degree weather.
									</li>
								</ul>
								<h2>Ingredients:</h2>
								<ul class="ingrdients">
									<li>
										¼ cup whole milk
									</li>
									<li>
										1 tablespoon unsweetened pumpkin puree
									</li>
									<li>
										1 ½ tablespoons granulated white sugar
									</li>
									<li>
										½ teaspoon vanilla extract
									</li>
									<li>
										½ teaspoon pumpkin pie spice
									</li>
									<li>
										1 cup brewed coffee
									</li>
									<li>
										¼ cup vanilla creamer
									</li>
									<li>
										Whipped cream and pumpkin pie spice for the top
									</li>
									
									
								</ul>
								<h2>Directions:</h2>
								<ul class="ingrdients">
									<li>
										1.	In a mixing cup, add milk, pumpkin spice puree, sugar, vanilla extract, and pumpkin pie spice. Use a whisk to combine them all well.
									</li>
									<li>
										2.	Place in the microwave and heat for 1 minute or in a small saucepan over medium heat (do not boil it, remove before it begins to boil). Remove and pour into your glass.
									</li>
									<li>
										3.	Brew 1 cup coffee and pour it into your glass. Whisk all of the ingredients together well.
									</li>
									<li>
										4.	Add vanilla creamer and top your drink off with whipped cream and a sprinkle of pumpkin pie spice.
									</li>
								</ul>
							</div>
