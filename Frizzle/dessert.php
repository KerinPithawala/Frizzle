								<h3>Iced Black Tea Lemonade</h3>
								<ul>
									<li>
										It’s a drink made of iced tea and lemonade that’s named after the famous golfer, Arnold Palmer. A very strong, sweet black tea and lemonade mix is sweetened and chilled to make a refreshing summer drink. A distinguished satisfying treat. A mix of iced tea and lemonade, an Arnold Palmer is a popular summertime drink. 
									</li>
								</ul>
								<h2>Ingredients:</h2>
								<ul class="ingrdients">
									<li>
										Black tea (loose or in tea sachets)
									</li>
									<li>
										Filtered water 
									</li>
									<li>
										Fresh lemon
									</li>
									<li>
										Sugar
									</li>
									<li>
										Ice
									</li>
									
								</ul>
								<h2>Directions:</h2>
								<ul class="ingrdients">
									<li>
										1.	Boil water in a tea kettle until it whistles.
									</li>
									<li>
										2.	Plunge tea bags beneath boiling water and let steep at least 5 minutes.
									</li>
									<li>
										3.	Pour lemonade concentrate into pitcher and, using the same can, add 2 cans of filtered water.
									</li>
									<li>
										4.	Mix until all concentrate is melted.
									</li>
									<li>
										5.	Add the black tea to the lemonade after it has steeped.
									</li>
									<li>
										6.	Add ice to the top. Refrigerate at least 2 hours.
									</li>
								</ul>
