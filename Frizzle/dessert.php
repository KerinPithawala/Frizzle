								<h3>Royal English Breakfast Tea Latte</h3>
								<ul>
									<li>
										A popular drink in Japan, Royal Milk Tea is made with Assam or Darjeeling tea leaves and milk. You can add sugar or honey to suit your taste. It’s a delicious drink to serve when you have friends over for tea time. Royal Mik Tea is really popular in Japan and typically available on the cafe menus, and you can also find bottles of royal milk tea in vending machines and convenience stores.
									</li>
								</ul>
								<h2>Ingredients:</h2>
								<ul class="ingrdients">
									<li>
										4 Cups Water
									</li>
									<li>
										1/2 Cup Milk 
									</li>
									<li>
										2 tbsp Teafloor’s high-quality English Breakfast Tea leaves
									</li>
									<li>
										1-2 Tsp Honey
									</li>
									<li>
										A pinch of cinnamon
									</li>
									
								</ul>
								<h2>Directions:</h2>
								<ul class="ingrdients">
									<li>
										1.	Take fresh filtered cold water and boil it in a kettle.
									</li>
									<li>
										2.	Use a bit of this boiling water to heat up your teapot by rinsing it with the water.
									</li>
									<li>
										3.	Add one tsp (per cup) of English Breakfast Tea leaves in the teapot.
									</li>
									<li>
										4.	Pour the boiling water over it.
									</li>
									<li>
										5.	Let the tea leaves infuse their goodness in the water by steeping them for 3-5 minutes.
									</li>
									<li>
										6.	Add a splash of milk according to your preference.
									</li>
									<li>
										7.	If you find your tea bitter, add some honey or agave nectar according to taste.
									</li>
									<li>
										8.	You can also add a pinch of cinnamon powder to top it.
									</li>
									<li>
										9.	And finally, pour the tea into the loveliest cups you have to thoroughly enjoy its essence
									</li>
								</ul>
